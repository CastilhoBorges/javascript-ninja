/*

O Condicional ternario substiui o IF e o ELSE
(CONDIÇÃO ? TRUE : FALSE) se for verdade ele para no bloco da ? se não cai no bloco do :

*/

var x = (1 === 2 ? true : false); 
console.log(x) // false 

// Se usace o padrão ia ficar bem maior 
if (1 === 2) {
    x = true
} else {
    x = false
}


